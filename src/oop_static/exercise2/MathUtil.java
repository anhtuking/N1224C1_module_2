package oop_static.exercise2;

public class MathUtil {
    static double cong(double a, double b) {
        return a + b;
    }

    static double tru(double a, double b) {
        return a - b;
    }

    static double nhan(double a, double b) {
        return a * b;
    }

    static double chia(double a, double b) {
        return a / b;
    }
}
